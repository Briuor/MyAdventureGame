    public Map(){
        this.row = map.length;
        this.col = map[0].length;
        this.mapLenX = tileSize * (col-1);
        this.mapLenY = tileSize * (row-1);
    }
    
    public void draw(Graphics g){
        int x = 0, y = 0;
        for(int i = 0; i < row; i++)
        {
            for(int j = 0; j < col; j++)
            {
                chooseElementColor(map[i][j], g);
                
                g.fillRect(x, y, tileSize, tileSize);
                x += 20; // next matrix element at same row
                
                //Draw next row
                if(x > mapLenX){
                    x = 0;
                    y+=20;
                }
            }
        }
    }
    
    public void chooseElementColor(char mapElement, Graphics g){
        switch(mapElement) {
            case TypeTile.WATER:   
                g.setColor(Color.blue);   break; // WATER  blue
            case TypeTile.GROUND:   
                g.setColor(Color.gray);   break; // GROUND gray
            default:
                g.setColor(Color.black);         // BLACK  NOTHING
        }
    }
